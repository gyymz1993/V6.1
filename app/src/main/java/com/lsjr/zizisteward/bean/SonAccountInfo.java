package com.lsjr.zizisteward.bean;

public class SonAccountInfo {

	private String son;
	private String son_limits;
	private String son_mobile;
	private String son_name;
	private String son_type;
	private String son_use_start;

	public String getSon() {
		return son;
	}

	public void setSon(String son) {
		this.son = son;
	}

	public String getSon_limits() {
		return son_limits;
	}

	public void setSon_limits(String son_limits) {
		this.son_limits = son_limits;
	}

	public String getSon_mobile() {
		return son_mobile;
	}

	public void setSon_mobile(String son_mobile) {
		this.son_mobile = son_mobile;
	}

	public String getSon_name() {
		return son_name;
	}

	public void setSon_name(String son_name) {
		this.son_name = son_name;
	}

	public String getSon_type() {
		return son_type;
	}

	public void setSon_type(String son_type) {
		this.son_type = son_type;
	}

	public String getSon_use_start() {
		return son_use_start;
	}

	public void setSon_use_start(String son_use_start) {
		this.son_use_start = son_use_start;
	}

}
